// Simple Hash Router
function initRouting() {
  const handleRoute = () => {
    const hash = window.location.hash || '#/';
    
    // Hide all sections
    document.querySelectorAll('.page-section').forEach(sec => {
      sec.classList.remove('active');
    });
    // Deactivate all nav links
    document.querySelectorAll('.nav-link').forEach(link => {
      link.classList.remove('active');
    });
    // Toggle target section and nav-link active states
    if (hash === '#/' || hash === '') {
      document.getElementById('page-landing').classList.add('active');
      document.getElementById('nav-home').classList.add('active');
    } else if (hash === '#/submit') {
      document.getElementById('page-submit').classList.add('active');
      document.getElementById('nav-submit').classList.add('active');
    } else if (hash === '#/my-submissions') {
      document.getElementById('page-my-submissions').classList.add('active');
      document.getElementById('nav-my-subs').classList.add('active');
      renderMySubmissions();
    } else if (hash === '#/dashboard') {
      document.getElementById('page-dashboard').classList.add('active');
      document.getElementById('nav-dashboard').classList.add('active');
      renderMPDashboard();
    }
    // Scroll window back to top
    window.scrollTo(0, 0);
  };
  window.addEventListener('hashchange', handleRoute);
  handleRoute(); // Run once initially
}
// Multi-language Swapping Implementation
function initLanguage() {
  const selector = document.getElementById('langSelector');
  
  // Retrieve saved language preference
  const savedLang = localStorage.getItem('janconnect_lang');
  if (savedLang && translations[savedLang]) {
    currentLanguage = savedLang;
    selector.value = savedLang;
  }
  
  applyLanguage(currentLanguage);
  selector.addEventListener('change', (e) => {
    currentLanguage = e.target.value;
    localStorage.setItem('janconnect_lang', currentLanguage);
    applyLanguage(currentLanguage);
    
    // Re-render components to update local table text descriptions and badge options
    renderMySubmissions();
    renderMPDashboard();
  });
}
function applyLanguage(lang) {
  const dictionary = translations[lang];
  if (!dictionary) return;
  // Swap all matching IDs
  Object.keys(dictionary).forEach(id => {
    const el = document.getElementById(id);
    if (el) {
      // Check if it is an input or select
      if (el.tagName === 'INPUT' && el.type === 'text') {
        el.placeholder = dictionary[id];
      } else if (el.tagName === 'TEXTAREA') {
        el.placeholder = dictionary[id];
      } else {
        el.textContent = dictionary[id];
      }
    }
  });
}
// Simulated Media Inputs (Audio recorder and hidden photo picker)
function initMediaSimulators() {
  const audioBtn = document.getElementById('audioBtn');
  const audioStatus = document.getElementById('audioStatus');
  const audioText = document.getElementById('audio-btn-txt');
  
  const photoBtn = document.getElementById('photoBtn');
  const photoText = document.getElementById('photo-btn-txt');
  // Create actual hidden file input for photo snapping simulation
  hiddenFileInput = document.createElement('input');
  hiddenFileInput.type = 'file';
  hiddenFileInput.accept = 'image/*';
  hiddenFileInput.style.display = 'none';
  document.body.appendChild(hiddenFileInput);
  // Audio simulation click
  audioBtn.addEventListener('click', () => {
    if (!isRecording && !tempAudioAttached) {
      // Start recording
      isRecording = true;
      secondsRecorded = 0;
      audioStatus.style.display = 'block';
      audioStatus.textContent = '00:00';
      audioBtn.classList.add('active');
      audioText.textContent = currentLanguage === 'hi' ? 'रिकॉर्डिंग... बंद करने के लिए क्लिक करें' : (currentLanguage === 'ta' ? 'பதிவு செய்யப்படுகிறது... நிறுத்த கிளிக் செய்க' : 'Recording... Click to Stop');
      
      mediaRecorderInterval = setInterval(() => {
        secondsRecorded++;
        const mins = String(Math.floor(secondsRecorded / 60)).padStart(2, '0');
        const secs = String(secondsRecorded % 60).padStart(2, '0');
        audioStatus.textContent = `${mins}:${secs}`;
      }, 1000);
    } else if (isRecording) {
      // Stop recording
      clearInterval(mediaRecorderInterval);
      isRecording = false;
      tempAudioAttached = true;
      tempAudioDuration = `${secondsRecorded}s`;
      
      audioText.textContent = currentLanguage === 'hi' ? `आवाज रिकॉर्डेड (${tempAudioDuration})` : (currentLanguage === 'ta' ? `குரல் பதிவு செய்யப்பட்டது (${tempAudioDuration})` : `Voice Recorded (${tempAudioDuration})`);
      audioStatus.textContent = currentLanguage === 'hi' ? 'क्लिक करके हटाएं' : (currentLanguage === 'ta' ? 'அகற்ற கிளிக் செய்க' : 'Click to Remove');
    } else {
      // Reset/Remove recording
      tempAudioAttached = false;
      tempAudioDuration = null;
      audioBtn.classList.remove('active');
      audioStatus.style.display = 'none';
      audioText.textContent = translations[currentLanguage]['audio-btn-txt'];
    }
  });
  // Photo uploading simulation click
  photoBtn.addEventListener('click', () => {
    if (!tempPhotoAttached) {
      hiddenFileInput.click();
    } else {
      // Remove attached photo
      tempPhotoAttached = false;
      tempPhotoName = "";
      photoBtn.classList.remove('active');
      photoText.textContent = translations[currentLanguage]['photo-btn-txt'];
    }
  });
  hiddenFileInput.addEventListener('change', (e) => {
    if (e.target.files && e.target.files[0]) {
      const file = e.target.files[0];
      tempPhotoAttached = true;
      tempPhotoName = file.name;
      photoBtn.classList.add('active');
      photoText.textContent = `${file.name} (Click to Remove)`;
    }
  });
}
