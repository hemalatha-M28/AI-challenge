// Compute dynamic summaries for MP stats board
function calculateDashboardStats() {
  const totalEl = document.getElementById('statTotal');
  const catEl = document.getElementById('statCategory');
  const areaEl = document.getElementById('statArea');
  
  if (!totalEl || !catEl || !areaEl) return;
  totalEl.textContent = submissions.length;
  if (submissions.length === 0) {
    catEl.textContent = 'None';
    areaEl.textContent = 'None';
    return;
  }
  // Calculate top category
  const categoriesCount = {};
  submissions.forEach(s => {
    categoriesCount[s.category] = (categoriesCount[s.category] || 0) + 1;
  });
  let topCategory = '';
  let maxCatCount = 0;
  Object.keys(categoriesCount).forEach(cat => {
    if (categoriesCount[cat] > maxCatCount) {
      maxCatCount = categoriesCount[cat];
      topCategory = cat;
    }
  });
  
  // Translate top category names if necessary for display
  catEl.textContent = topCategory || 'None';
  // Calculate top area (extracting base Ward/Area name to group similar)
  const areasCount = {};
  submissions.forEach(s => {
    // Normalize string slightly for counting: e.g. "Ward 12, Karol Bagh" -> "Karol Bagh" or keep exact
    // Keeping exact gives a realistic view
    const loc = s.location.trim();
    areasCount[loc] = (areasCount[loc] || 0) + 1;
  });
  let topArea = '';
  let maxAreaCount = 0;
  Object.keys(areasCount).forEach(area => {
    if (areasCount[area] > maxAreaCount) {
      maxAreaCount = areasCount[area];
      topArea = area;
    }
  });
  areaEl.textContent = topArea || 'None';
}
// Populate Area/Category dropdown filters dynamically from database
let filtersPopulated = false;
function populateFilterOptions() {
  const filterCat = document.getElementById('filterCategory');
  const filterArea = document.getElementById('filterArea');
  if (!filterCat || !filterArea || filtersPopulated) return;
  // Extract unique categories
  const categories = [...new Set(submissions.map(item => item.category))];
  // Extract unique areas
  const areas = [...new Set(submissions.map(item => item.location))];
  // Save selected values before reloading
  const prevCat = filterCat.value;
  const prevArea = filterArea.value;
  filterCat.innerHTML = `<option value="">All Categories</option>`;
  categories.forEach(cat => {
    filterCat.innerHTML += `<option value="${cat}">${cat}</option>`;
  });
  filterArea.innerHTML = `<option value="">All Areas</option>`;
  areas.forEach(area => {
    filterArea.innerHTML += `<option value="${area}">${area}</option>`;
  });
  // Restore values
  filterCat.value = prevCat;
  filterArea.value = prevArea;
  
  filtersPopulated = true;
}
// Get filtered and sorted list of submissions
function getFilteredAndSortedSubmissions() {
  const searchVal = document.getElementById('dashboardSearch').value.toLowerCase();
  const categoryVal = document.getElementById('filterCategory').value;
  const areaVal = document.getElementById('filterArea').value;
  const sortVal = document.getElementById('filterSort').value;
  let results = [...submissions];
  // Category filter
  if (categoryVal) {
    results = results.filter(item => item.category === categoryVal);
  }
  // Area filter
  if (areaVal) {
    results = results.filter(item => item.location === areaVal);
  }
  // Keyword search
  if (searchVal) {
    results = results.filter(item => {
      return item.id.toLowerCase().includes(searchVal) ||
             item.location.toLowerCase().includes(searchVal) ||
             item.description.toLowerCase().includes(searchVal) ||
             item.category.toLowerCase().includes(searchVal);
    });
  }
  // Sort
  if (sortVal === 'upvotes') {
    // Rank by upvotes (highest first)
    results.sort((a, b) => b.upvotes - a.upvotes);
  } else if (sortVal === 'date') {
    // Rank by date (newest first)
    results.sort((a, b) => new Date(b.date) - new Date(a.date));
  }
  return results;
}
// Update MP suggestion status dropdown directly in table
window.updateSubmissionStatus = function(id, selectElement) {
  const index = submissions.findIndex(item => item.id === id);
  if (index === -1) return;
  submissions[index].status = selectElement.value;
  saveSubmissions();
  // Reset category class
  selectElement.className = 'mp-status-select';
  if (selectElement.value === 'Under Review') {
    selectElement.classList.add('status-review');
  } else if (selectElement.value === 'Prioritized') {
    selectElement.classList.add('status-prioritized');
  } else {
    selectElement.classList.add('status-received');
  }
  // Sync details modal if open
  const modal = document.getElementById('detailsModal');
  if (modal.classList.contains('visible') && activeModalId === id) {
    populateModal(submissions[index]);
  }
  
  // Re-render my submissions so the status is synced there instantly
  renderMySubmissions();
};
