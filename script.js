// Formatting Dates
function formatDate(dateStr) {
  const options = { year: 'numeric', month: 'short', day: 'numeric' };
  const d = new Date(dateStr);
  return d.toLocaleDateString(currentLanguage === 'en' ? 'en-US' : (currentLanguage === 'hi' ? 'hi-IN' : 'ta-IN'), options);
}
// Global toggle upvote trigger
window.toggleUpvote = function(id) {
  const index = submissions.findIndex(item => item.id === id);
  if (index === -1) return;
  const voteIndex = upvotedIds.indexOf(id);
  if (voteIndex === -1) {
    // Upvote
    submissions[index].upvotes += 1;
    upvotedIds.push(id);
  } else {
    // Remove upvote
    submissions[index].upvotes -= 1;
    upvotedIds.splice(voteIndex, 1);
  }
  saveSubmissions();
  saveUpvotes();
  
  // Update renders
  renderMySubmissions();
  renderMPDashboard();
  
  // If modal is open, refresh it as well
  const modal = document.getElementById('detailsModal');
  if (modal.classList.contains('visible') && activeModalId === id) {
    populateModal(submissions[index]);
  }
};
